    public void tampil_data(){
    DefaultTableModel tabel=new DefaultTableModel();
    tabel.addColumn("NISN");
    tabel.addColumn("NAMA");
    tabel.addColumn("GENDER");
    tabel.addColumn("AGAMA");
    tabel.addColumn("TEMPAT");
    tabel.addColumn("TGL LAHIR");
    tabel.addColumn("JURUSAN");
    tabel.addColumn("ALAMAT");
    tabel.addColumn("TELP");
    try {
        java.sql.Connection conn=(java.sql.Connection)final_oop.koneksi.koneksiDB();
        String sql="select *from siswa";
        java.sql.PreparedStatement pst =conn.prepareStatement(sql);
        ResultSet rs=pst.executeQuery(sql);
        while (rs.next())
        {
        tabel.addRow(new Object[]{
            rs.getString(1),
            rs.getString(2),
            rs.getString(3),
            rs.getString(4),
            rs.getString(5),
            rs.getString(6),
            rs.getString(7),
            rs.getString(8),
            rs.getString(9)});
        }
        jTable1.setModel(tabel);
        }
    catch (SQLException e){
    }
}
