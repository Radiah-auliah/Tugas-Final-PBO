    private void bt_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_cariActionPerformed
        // TODO add your handling code here:
   try {
    java.sql.Connection conn;
       conn = (java.sql.Connection)final_oop.koneksi.koneksiDB();

   String sql = "Select * from siswa where nisn='"+cari.getText()+"'";
   java.sql.PreparedStatement pst = conn.prepareStatement(sql);
   ResultSet rs = pst.executeQuery(sql);
    if (rs.next())
    {
        nisn.setText(rs.getString(1));
        nama.setText(rs.getString(2));
        gender.setSelectedItem(rs.getString(3));
        agama.setText(rs.getString(4));
        tempat.setText(rs.getString(5));
        tgl.setDate(rs.getDate(6));
        jurusan.setText(rs.getString(7));
        alamat.setText(rs.getString(8));
        hp.setText(rs.getString(9));
        JOptionPane.showMessageDialog(null,"Data Siswa : "
        + cari.getText() + " ditemukan, Apakah Anda Ingin Mengedit atau Menghapus?");
        }
        } catch (Exception e){}
            cari.requestFocus();
    }//GEN-LAST:event_bt_cariActionPerformed

